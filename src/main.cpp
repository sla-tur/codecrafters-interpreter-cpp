std::string read_file_contents(const std::string& filename) {
    std::ifstream file(filename);
    if (!file.is_open()) {
        std::cerr << "Error reading file: " << filename << std::endl;
        std::exit(1);
    }   

    std::stringstream buffer;
    buffer << file.rdbuf();
    file.close();

    return buffer.str();
}
